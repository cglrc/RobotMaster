#include "InHpp.hpp"
#include "timers.h"

// 定时器句柄
TimerHandle_t xNoteTimer;
TimerHandle_t xRestTimer;

uint16_t psc = 0;
uint16_t pwm = MIN_BUZZER_PWM;

uint8_t playState = 1;
bool isBuzzerPlaying = false;
uint32_t playIndex = 0;
float noteDuration;

// 现在的 current_song 会在歌曲数组定义完后再赋值
extern const Song_t* current_song;

uint32_t timFrequency_;

void NoteTimerCallback(TimerHandle_t xTimer);
void RestTimerCallback(TimerHandle_t xTimer);
void Handle_Next_Note(void);

// 当前播放的歌曲指针 (直接由外部宏 ACTIVE_SONG 映射)
const Song_t* current_song = &ACTIVE_SONG;

// 初始化定时器（在程序初始化时调用）
void Buzzer_Timers_Init(void)
{
    // 开始PWM输出
    HAL_TIM_PWM_Start(&htim12, TIM_CHANNEL_1);
    // TIM的计数频率
    timFrequency_ = TIM_GetCounterFreq(&htim12);
    // 每拍的持续时间 (自动读取当前选用歌曲的BPM)
    noteDuration = 1000.0f * 60.0f / current_song->bpm;

    xNoteTimer = xTimerCreate("NoteTimer", pdMS_TO_TICKS(1), pdFALSE, NULL, NoteTimerCallback);
    xRestTimer = xTimerCreate("RestTimer", pdMS_TO_TICKS(1), pdFALSE, NULL, RestTimerCallback);
    
    // 检查定时器是否创建成功
    if (xNoteTimer == NULL || xRestTimer == NULL) {
        // 定时器创建失败处理
        Error_Handler();
    }
}

// 音符定时器回调函数
void NoteTimerCallback(TimerHandle_t xTimer)
{
    // 停止当前音符（静音）
    __HAL_TIM_SET_COMPARE(&htim12, TIM_CHANNEL_1, 0);
    // 启动静音定时器（5ms）
    xTimerChangePeriod(xRestTimer, pdMS_TO_TICKS(5), portMAX_DELAY);
    xTimerStart(xRestTimer, portMAX_DELAY);
}

// 静音定时器回调函数
void RestTimerCallback(TimerHandle_t xTimer)
{
    // 播放下一个音符
    playIndex++;
    if (playIndex >= current_song->length) {
        playState = 0;
        playIndex = 0;
        isBuzzerPlaying = false; // 标记播放彻底结束
        // 不需要显式去Stop，单次定时器自动停止
    } else {
        // 直接处理下一个音符（避免任务调度延迟）
        Handle_Next_Note();
    }
}

// 处理下一个音符（无延时）
void Handle_Next_Note(void)
{
    const Bate bate = current_song->notes[playIndex];
    uint32_t note_ms = (uint32_t)(bate.period * noteDuration);
    if (note_ms <= 5) note_ms = 6; // 防止短音符时间下溢出变成负数或者0

    if (bate.frequency == P0) {
        __HAL_TIM_SET_COMPARE(&htim12, TIM_CHANNEL_1, 0);
        // 对于休止符，也启动定时器来处理时长
        xTimerChangePeriod(xNoteTimer, pdMS_TO_TICKS(note_ms - 5), portMAX_DELAY);
    } else {
        uint32_t arr = timFrequency_ / bate.frequency;
        __HAL_TIM_SET_AUTORELOAD(&htim12, arr);
        // 调整占空比，默认20%(原版)是短促有力的拨弦感，如果想声音连贯可以改为 / 2 (50%方形波) 或者 / 4
        // 原版这里是 arr / 5 
        __HAL_TIM_SET_COMPARE(&htim12, TIM_CHANNEL_1, arr / 5);
        __HAL_TIM_SetCounter(&htim12, 0);
        // 启动音符定时器（减去5ms的静音间隔）
        xTimerChangePeriod(xNoteTimer, pdMS_TO_TICKS(note_ms - 5), portMAX_DELAY);
    }
}

// 主处理函数（在任务中每1ms循环调用）
void Handle_Buzzer(void)
{
    // 极其严谨的状态机：避免定时器命令进入队列时的查询窗口导致重复触发
    if (playState == 1 && !isBuzzerPlaying) {
        isBuzzerPlaying = true; // 立刻锁上门，防止下1ms再次跑进来重复开枪
        Handle_Next_Note(); // 踢出第一脚
    } else if (playState == 0 && isBuzzerPlaying) {
        isBuzzerPlaying = false;
        xTimerStop(xNoteTimer, 0);
        xTimerStop(xRestTimer, 0);
        __HAL_TIM_SET_COMPARE(&htim12, TIM_CHANNEL_1, 0);
    }
    
    // 检查电机掉线：如果正在播放时电机掉线，则停止播放
    if (all_motors_connected == false && isBuzzerPlaying) {
        playState = 0;
        isBuzzerPlaying = false; // 强制复位内部播放状态
        
        // 停止音乐播放定时器
        xTimerStop(xNoteTimer, 0);
        xTimerStop(xRestTimer, 0);
        __HAL_TIM_SET_COMPARE(&htim12, TIM_CHANNEL_1, 0);
    }
}

